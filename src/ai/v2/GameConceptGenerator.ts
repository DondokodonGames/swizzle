  /**
   * ランダムにテーマを選択（JSONファイルから読み込んだテーマを使用）
   * 組み合わせではなく、事前定義されたオリジナルテーマを使用
   */
  private selectRandomTheme(): string {
    // 使用済みでないテーマを抽出
    const availableThemes = LOADED_THEMES.filter(t => !this.usedThemes.has(t.toLowerCase()));

    if (availableThemes.length === 0) {
      // 全テーマ使い切った場合はリセット
      this.usedThemes.clear();
      return LOADED_THEMES[Math.floor(Math.random() * LOADED_THEMES.length)];
    }

    // ランダムに1つ選択
    return availableThemes[Math.floor(Math.random() * availableThemes.length)];
  }

  /**
   * ゲームコンセプトを生成
   */
  async generate(feedback?: string): Promise<GameConcept> {
    if (this.config.dryRun) {
      return this.generateMockConcept();
    }

    // コード側でテーマをランダム選択（AIの偏りを防ぐ）
    const forcedTheme = this.selectRandomTheme();
    console.log(`      🎲 Selected theme: ${forcedTheme}`);

    let prompt = CONCEPT_PROMPT;

    // 強制テーマを追加
    prompt += `\n\n# 今回のテーマ（必ずこのテーマを使用してください）\nテーマ: 「${forcedTheme}」\n\nこのテーマに基づいたゲームを考えてください。テーマを変更しないでください。`;

    // 既存テーマを避けるための追加指示
    if (this.usedThemes.size > 0) {
      prompt += `\n\n# 避けるべきテーマ（既出）\n${Array.from(this.usedThemes).join(', ')}`;
    }

    // フィードバックがある場合（再生成時）
    if (feedback) {
      prompt += `\n\n# 前回の問題点\n${feedback}\n\n上記の問題を解決したコンセプトを生成してください。`;
    }

    const response = await this.client.messages.create({
      model: this.config.model,
      max_tokens: 1024,
      messages: [{ role: 'user', content: prompt }]
    });

    const content = response.content[0];
    if (content.type !== 'text') {
      throw new Error('Unexpected response type');
    }

    // JSONを抽出
    const jsonMatch = content.text.match(/\{[\s\S]*\}/);
    if (!jsonMatch) {
      throw new Error('No JSON found in response');
    }

    const concept = JSON.parse(jsonMatch[0]) as GameConcept;

    // スコアチェック
    const { goalClarity, operationClarity, judgmentClarity, acceptance } = concept.selfEvaluation;
    const allAboveMin = [goalClarity, operationClarity, judgmentClarity, acceptance]
      .every(score => score >= this.config.minScore);

    if (!allAboveMin) {
      throw new Error(`Self-evaluation scores below minimum (${this.config.minScore}): ${JSON.stringify(concept.selfEvaluation)}`);
    }

    // テーマを記録
    this.usedThemes.add(concept.theme);

    return concept;
  }

